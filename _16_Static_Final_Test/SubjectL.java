package _16_Static_Final_Test;

public class SubjectL {
	// 과목정보은 과목명 강의실 담당교수가 있다.
	private String name = null;
	private String num = null;
	private String proName = null;
	
	
	
	String getName() {
		return name;
	}
	
	void setName(String name) {
		this.name = name;
	}
	
	String getNum() {
		return num;
	}
	
	void setNum(String num) {
		this.num = num;
	}
	
	String getProName() {
		return proName;
	}
	
	void setProName(String proName) {
		this.proName = proName;
	}
	
	
	
	
	
}
